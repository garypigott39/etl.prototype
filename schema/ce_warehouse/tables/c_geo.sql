 * Control table - geography details, used for validation & extra detail.
 *
 * Originally just a lookup table, but due to its complexity it's been added to the control tables.
 ***********************************************************************************************************
 */

-- DROP TABLE IF EXISTS ce_warehouse.c_geo;

CREATE TABLE IF NOT EXISTS ce_warehouse.c_geo
(
    pk_geo INT GENERATED BY DEFAULT AS IDENTITY,

    code TEXT NOT NULL
        CHECK (code ~ '^G\.[A-Z0-9_]+[A-Z0-9]$' AND code !~ 'INTERNAL'),
    name TEXT NOT NULL
        CHECK (ce_warehouse.fx_val_is_text(name, FALSE) IS NULL),
    name2 TEXT NOT NULL
        CHECK (ce_warehouse.fx_val_is_text(name2, FALSE) IS NULL),
    short_name TEXT NOT NULL
        CHECK (ce_warehouse.fx_val_is_text(short_name) IS NULL),
    tla TEXT NOT NULL
        CHECK (ce_warehouse.fx_val_is_text(tla, FALSE) IS NULL),
    iso2 TEXT
        CHECK (iso2 ~ '^[A-Z]{2}$'),
    iso3 TEXT
        CHECK (iso3 ~ '^[A-Z]{3}$'),
    lat NUMERIC
        CHECK (lat >= -90 AND lat <= 90),
    long NUMERIC
        CHECK (long >= -180 AND long <= 180),
    central_bank TEXT
        REFERENCES ce_warehouse.l_central_bank(code),
            DEFERRABLE INITIALLY DEFERRED
            ON UPDATE CASCADE
            ON DELETE RESTRICT,
    stock_market TEXT
        REFERENCES ce_warehouse.l_stock_market(code),
            DEFERRABLE INITIALLY DEFERRED
            ON UPDATE CASCADE
            ON DELETE RESTRICT,
    political_alignment TEXT
        REFERENCES ce_warehouse.l_political_alignment(code),
            DEFERRABLE INITIALLY DEFERRED
            ON UPDATE CASCADE
            ON DELETE RESTRICT,
    local_currency_unit TEXT
        REFERENCES ce_warehouse.l_currency_unit(code),
            DEFERRABLE INITIALLY DEFERRED
            ON UPDATE CASCADE
            ON DELETE RESTRICT,
    flag TEXT
        CHECK (
            ce_warehouse.fx_val_is_text(flag) IS NULL AND
            flag ~ '^(?!https?://)[\w\-/]+\/?[\w\-]+\.(jpg|jpeg|png|gif|webp)$'
        ),
    category TEXT NOT NULL
        REFERENCES ce_warehouse.l_geo_category(code)
            DEFERRABLE INITIALLY DEFERRED
            ON UPDATE CASCADE
            ON DELETE RESTRICT,

    ordering INT NOT NULL DEFAULT 0,
    internal_notes TEXT
        CHECK (ce_warehouse.fx_val_is_text(internal_notes) IS NULL),

    updated_utc TIMESTAMPTZ NOT NULL DEFAULT NOW(),

    PRIMARY KEY (pk_geo),
    UNIQUE (code)
);

COMMENT ON TABLE ce_warehouse.c_geo
    IS 'Control table - geography details, used for validation & extra detail';

