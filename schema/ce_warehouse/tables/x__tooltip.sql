-- DROP TABLE IF EXISTS ce_warehouse.x__tooltip;

CREATE TABLE IF NOT EXISTS ce_warehouse.x__tooltip
(
    pk_tip INT NOT NULL GENERATED BY DEFAULT AS IDENTITY,

    tooltip TEXT NOT NULL
        CHECK (ce_warehouse.fx_val__is_text(tooltip, 'x_tooltip.tooltip', FALSE) IS NULL),

    PRIMARY KEY(pk_tip),
    UNIQUE (tooltip)
);

COMMENT ON TABLE ce_warehouse.x__tooltip
    IS 'Internal table - Tooltip values';
