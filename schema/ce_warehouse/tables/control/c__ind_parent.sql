/*
 ***********************************************************************************************************
 * @file
 * c_ind_parent.sql
 *
 * Control table - IND parents.
 * Replaces the "parent_icodes" array in c_ind with a more structured table, allowing for better maintenance.
 ***********************************************************************************************************
 */

-- DROP TABLE IF EXISTS ce_warehouse.c__ind_parent;

CREATE TABLE IF NOT EXISTS ce_warehouse.c__ind_parent
(
    idx INT GENERATED BY DEFAULT AS IDENTITY,

    fk_pk_ind INT NOT NULL
        REFERENCES ce_warehouse.c__ind (pk_ind)
            ON DELETE CASCADE
            DEFERRABLE INITIALLY DEFERRED,

    fk_pk_ind_parent INT NOT NULL
        REFERENCES ce_warehouse.c__ind (pk_ind)
            ON DELETE CASCADE
            DEFERRABLE INITIALLY DEFERRED
        CHECK (
            fk_pk_ind > 0
            AND fk_pk_ind_parent > 0
            AND fk_pk_ind_parent <> fk_pk_ind  -- prevent self-reference
        ),

    error TEXT,  -- system generated
    ts_updated TIMESTAMPTZ NOT NULL DEFAULT NOW(),

    PRIMARY KEY (idx),
    UNIQUE (fk_pk_ind, fk_pk_ind_parent)
);

-- It's recommended to have INDICES on foreign keys for performance!!
-- unless we already have them on the referenced table
CREATE INDEX IF NOT EXISTS c_ind__parent__idx
    ON ce_warehouse.c__ind_parent (fk_pk_ind_parent);

COMMENT ON TABLE ce_warehouse.c__ind_parent
    IS 'Control table - IND parents';
