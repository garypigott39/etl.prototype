-- DROP TABLE IF EXISTS ce_warehouse.c_com;

CREATE TABLE IF NOT EXISTS ce_warehouse.c_com
(
    pk_com INT GENERATED BY DEFAULT AS IDENTITY,

    code TEXT NOT NULL
        CHECK (ce_warehouse.fx_val_is_geo_code(code, FALSE) IS NULL),
    name TEXT NOT NULL
        CHECK (ce_warehouse.fx_val_is_text(name, FALSE) IS NULL),
    short_name TEXT NOT NULL
        CHECK (ce_warehouse.fx_val_is_text(short_name, FALSE) IS NULL),
    tla TEXT NOT NULL
        CHECK (ce_warehouse.fx_val_is_text(tla, FALSE) IS NULL),
    commodity_type TEXT NOT NULL
        REFERENCES ce_warehouse.c_com_type(code)
            ON UPDATE CASCADE
            ON DELETE RESTRICT
            DEFERRABLE INITIALLY DEFERRED,
    ordering INT NOT NULL DEFAULT '0',

    internal_notes TEXT
        CHECK (ce_warehouse.fx_val_is_text(internal_notes) IS NULL),

    updated_utc TIMESTAMPTZ NOT NULL DEFAULT NOW(),

    PRIMARY KEY(pk_com),
    UNIQUE (code)
);

COMMENT ON TABLE ce_warehouse.c_com
      IS 'Control table - commodity details, used for validation & extra detail';
