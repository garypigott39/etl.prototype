VALUES
    (-1, 'migration', -1, 'System user for data migration and backfilling'),
    (-2, 'app', -2, 'System user for automated processes');

/*
 ***********************************************************************************************************
 * Block any updates to system records
 ***********************************************************************************************************
 */

-- DROP TRIGGER IF EXISTS tg__luser__b01 ON ce_warehouse.l__user;

CREATE TRIGGER tg__luser__b01
    BEFORE UPDATE OR DELETE
        ON ce_warehouse.l__user
    FOR EACH ROW
        EXECUTE FUNCTION ce_warehouse.fx_tg__utils__block_internal('pk_user');

COMMENT ON TRIGGER tg__luser__b01 ON ce_warehouse.l__user
    IS 'Trigger to block changes to system records on l__user table';
