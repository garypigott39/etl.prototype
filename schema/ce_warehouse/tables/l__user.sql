 * Lookup table - provides a link to app users for auditing and other purposes. System generated.
 ***********************************************************************************************************
 */

-- DROP TABLE IF EXISTS ce_warehouse.l__user;

CREATE TABLE IF NOT EXISTS ce_warehouse.l__user
(
    pk_user INT NOT NULL GENERATED BY DEFAULT AS IDENTITY,

    name TEXT NOT NULL
        CHECK (name <> ''),  -- Ensure that the name is not empty
    status TEXT NOT NULL DEFAULT 'active'
        CHECK (status IN ('active', 'inactive', 'deleted')),
    source_uid INT NOT NULL,  -- Store the source user ID from the application, for reference

    -- Additional cutom fields
    is_admin BOOL NOT NULL DEFAULT FALSE,  -- Admin user
    is_data_team BOOL NOT NULL DEFAULT FALSE,  -- Member of the data team
    is_value_uploader BOOL NOT NULL DEFAULT FALSE,  -- User with financial upload permissions
    is_checking_required BOOL NOT NULL DEFAULT FALSE,  -- User value uploads require "checking"

    internal_notes TEXT
        CHECK (ce_warehouse.fx_val__is_text(internal_notes, 'internal_notes') IS NULL),

    ts_created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),

    PRIMARY KEY (pk_user),
    UNIQUE (source_uid)
);

COMMENT ON TABLE ce_warehouse.l__user
    IS 'Lookup table - (series) units lookup';

/**
 * Pre-populate with System Use values.
 */
